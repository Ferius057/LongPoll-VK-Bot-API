package Entities;

public class Server {
    String key;
    String server;
    String ts;

    public void setKey(String key) {
        this.key = key;
    }

    public void setServer(String server) {
        this.server = server;
    }

    public void setTs(String ts) {
        this.ts = ts;
    }

    public String getKey() {
        return key;
    }

    public String getServer() {
        return server;
    }

    public String getTs() {
        return ts;
    }
}
